<template>
  <h1>Hello Everyone</h1>
 <p> {{ contador }}</p>
<button @click="incrementar">Incrementar</button> <span></span>
<button @click="decrementar">Decrementar</button>
</template>

<script setup>
import { ref } from 'vue';
const contador = ref(0);
const incrementar = () => {
  contador.value++;
}

const decrementar = () => {
  contador.value--;
}

</script>

<style scoped>
p {
  color:blue;
}
</style>
